                                </h4>
                            </div>
                            <div id="collapseEight" class="panel-collapse collapse">
                                <div class="panel-body">
                                    After blood has been drawn, it is tested for ABO group (blood type) and Rh type (positive or negative), as well as for any unexpected red blood cell antibodies that may cause problems in a recipient. Screening tests also are performed for evidence of donor infection with hepatitis B and C viruses, human immunodeficiency viruses HIV-1 and HIV-2, human T-lymphotropic viruses HTLV-I and HTLV-II, West Nile virus (WNV), and syphilis. Each donor is tested at least one time for antibodies to Trypanosoma cruzi, the parasite that causes Chagas disease.<br>
The specific tests currently performed are listed below: <br>
Hepatitis B surface antigen (HBsAg)<br>
Hepatitis B core antibody (anti-HBc)<br>
Hepatitis C virus antibody (anti-HCV)<br>
HIV-1 and HIV-2 antibody (anti-HIV-1 and anti-HIV-2)<br>
HTLV-I and HTLV-II antibody (anti-HTLV-I and anti-HTLV-II)<br>
Serologic test for syphilis<br>
Nucleic acid amplification testing (NAT) for HIV-1 ribonucleic acid (RNA), HCV RNA and WNV RNA <br>
Nucleic acid amplification testing (NAT) for HBV deoxyribonucleic acid <br>
Antibody test for Trypanosoma cruzi, the agent of Chagas disease  

